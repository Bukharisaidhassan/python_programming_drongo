b = ['data', 'and', 'book', 'hello']
# adding 32 to the list 
b += [32]
print(b)
 
# removing an element from the list 
b[2:4] = []
print(b)
new_list = b[5:]
print(new_list)

# delete the element at the first index
del b[0]
print(b)

# accessing element at different index 
a = ['date', 'structures', 'using', 'python', 'learning']
print(a[0])
print(a[2])
print(a[-1])
print(a[-5])
print(a[1:5])
print(a[-3:-1])




